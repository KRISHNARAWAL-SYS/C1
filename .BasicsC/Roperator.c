#include<stdio.h>
#include<math.h>
// relational operators
//==  equal to
//>,>=  greater than or greater than equal to
//<,<=  less than or less than equal to
// !=   not equal to
int main(){
    printf("%d \n", 4>3);
    printf("%d \n", 4 == 4);
    printf("%d \n", 4 != 4);
    printf("%d \n", 4 < 6);
    printf("%d \n", 4 <= 6);
    return 0 ;





}